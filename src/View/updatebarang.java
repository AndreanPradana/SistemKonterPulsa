    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            barang b = new barang();
            b.setJenis_barang(control.getDataJenisBarang().get(cbInsertJenisBarang.getSelectedIndex()));
            b.setNama_barang(tfInsertNamaBarang.getText());
            b.setHarga_barang(Integer.parseInt(tfInsertHargaBarang.getText()));
            b.setStock_barang(Integer.parseInt(tfInsertStockBarang.getText()));
            
            control.insertBarang(b);
            showTableBarang();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(updatebarang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            barang b = control.getDataBarang().get(tblBarang.getSelectedRow());
            control.deleteBarang(b);
            showTableBarang();
        } catch (SQLException ex) {
            Logger.getLogger(updatejenis_barang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

