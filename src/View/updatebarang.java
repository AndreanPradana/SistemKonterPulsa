    public void showTableBarang() throws SQLException{
        DefaultTableModel dtmbarang = new DefaultTableModel(new String[]{"NAMA BARANG", "STOCK BARANG", "HARGA BARANG"},0);
        
        for(barang b : control.getDataBarang()){
            dtmbarang.addRow(new String[]{b.getNama_barang().toString(), b.getStock_barang().toString(), b.getHarga_barang().toString()});
        }
        tblBarang.setModel(dtmbarang);
    }
    
    public void showcomboBoxJenisBarang() throws SQLException{
        DefaultComboBoxModel dcbmJenisBarang = new DefaultComboBoxModel();
        
        for(jenis_barang jb : control.getDataJenisBarang()){
            dcbmJenisBarang.addElement(jb.getNama_jenis_barang());
        }
        this.cbInsertJenisBarang.setModel(dcbmJenisBarang);
    }

