    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            barang b = control.getDataBarang().get(tblBarang.getSelectedRow());
            int Stock = Integer.parseInt(tfUpdateStockBarang.getText());
            int harga = Integer.parseInt(tfUpdateHargaBarang.getText());
            
            control.updateBarang(b, Stock, harga);
            showTableBarang();
        } catch (SQLException ex) {
            Logger.getLogger(updatebarang.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

